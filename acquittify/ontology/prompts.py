from __future__ import annotations

import json


def build_structured_extraction_prompt(opinion_text: str, resolved_citations: list[dict] | None = None) -> str:
    citations_block = json.dumps(resolved_citations or [], ensure_ascii=False)
    return (
        "You are extracting a precedential ontology from a judicial opinion. "
        "Return ONLY valid JSON with this top-level schema: "
        "{\"holdings\": [...], \"issues\": [...], \"relations\": [...], \"interpretive_edges\": [...]} "
        "OR strict alias {\"edges\": [...]} for interpretive edges only. "
        "No prose, no markdown, no comments. "
        "Use citation IDs from the resolved citation list when available.\n\n"
        "Holdings schema item:\n"
        "{\"holding_text\": str, \"if_condition\": [{\"predicate\": str, \"value\": any}], "
        "\"then_consequence\": [{\"predicate\": str, \"value\": any}], "
        "\"normative_strength\": \"binding_core|binding_narrow|persuasive|dicta\", "
        "\"normative_source\": [str], "
        "\"fact_vector\": [{\"dimension\": str, \"value\": any}], "
        "\"secondary_sources\": [{\"source_id\": str, \"title\": str|null, \"topic_tags\": [str]}], "
        "\"citations_supporting\": [str]}\n\n"
        "Issues schema item:\n"
        "{\"normalized_form\": str, \"taxonomy\": {\"domain\": str, \"doctrine\": str, \"rule_type\": str}, "
        "\"required_fact_dimensions\": [str], "
        "\"supporting_citations\": [str]}\n\n"
        "Relations schema item:\n"
        "{\"source_holding_index\": int|null, \"target_holding_index\": int|null, "
        "\"source_holding_id\": str|null, \"target_holding_id\": str|null, "
        "\"relation_type\": \"applies|clarifies|extends|distinguishes|limits|overrules|questions\", "
        "\"citation_type\": \"controlling|persuasive|background\", "
        "\"confidence\": float, \"evidence_span\": {\"start_char\": int, \"end_char\": int}}\n\n"
        "Interpretive edge schema item:\n"
        "{\"source_case\": str, \"target_authority\": str, "
        "\"authority_type\": \"CONSTITUTION|STATUTE|REGULATION|FEDERAL_RULE|PRIOR_CASE\", "
        "\"edge_type\": "
        "\"EXTENDS_AMENDMENT|NARROWS_AMENDMENT|BROADENS_AMENDMENT|APPLIES_AMENDMENT|EXPLAINS_AMENDMENT|"
        "CLARIFIES_DOCTRINE|INVALIDATES_STATUTE_UNDER|INVALIDATES_REGULATION_UNDER|UPHOLDS_STATUTE_AGAINST|"
        "REJECTS_CONSTITUTIONAL_CHALLENGE|RECOGNIZES_RIGHT_UNDER|LIMITS_AMENDMENT_SCOPE|QUESTIONS_PRECEDENT_UNDER|"
        "OVERRULES_PRECEDENT_UNDER|INTERPRETS_STATUTE|BROADENS_STATUTE|NARROWS_STATUTE|APPLIES_PLAIN_MEANING|"
        "USES_LEGISLATIVE_HISTORY|APPLIES_LENITY|APPLIES_CONSTITUTIONAL_AVOIDANCE|FINDS_STATUTE_AMBIGUOUS|"
        "RESOLVES_STATUTORY_AMBIGUITY|INVALIDATES_STATUTE|SEVERS_PROVISION|DISTINGUISHES_STATUTE|EXTENDS_STATUTE|"
        "REJECTS_EXPANSIVE_READING|CONSTRUES_TO_AVOID_CONSTITUTIONAL_ISSUE\", "
        "\"confidence\": float, \"text_span\": str}\n\n"
        "Interpretive edge rules:\n"
        "- Create an interpretive edge only when this court makes the move, not when merely quoting prior authority.\n"
        "- Multiple interpretive moves in one opinion should produce multiple edges.\n"
        "- Constitutional edge targets should be normalized forms such as \"U.S. Const. amend. IV\".\n"
        "- Statutory edge targets should be normalized full forms such as \"18 U.S.C. § 922(g)(1)\".\n"
        "- Federal rules should normalize to forms such as \"Fed. R. Crim. P. 29\".\n"
        "- Prior-case targets should use reporter citation text if available.\n\n"
        "Common constitutional signals include phrases like: "
        "\"violates the Fourth Amendment\", \"under the Fifth Amendment\", "
        "\"unconstitutional as applied\", \"facial challenge\", \"strict scrutiny\", "
        "\"equal protection\", \"due process requires\", \"we decline to expand\".\n"
        "Common statutory signals include phrases like: "
        "\"plain meaning\", \"Congress intended\", \"ambiguous\", \"rule of lenity\", "
        "\"statutory construction\", \"text and structure\", \"constitutional avoidance\".\n\n"
        f"Resolved citations:\n{citations_block}\n\n"
        "Opinion text:\n"
        f"{opinion_text}"
    )
